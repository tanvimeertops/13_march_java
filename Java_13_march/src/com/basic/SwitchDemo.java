package com.basic;

public class SwitchDemo {
public static void main(String[] args) {
	int choice=4;
	
	switch (choice) {
	case 1:
		System.out.println("Sunday");
		break;
	case 2:
		System.out.println("Monday");
		break;
	case 3:
		System.out.println("Tuesday");
		break;
	case 4:
		System.out.println("Wednesday");
//		break;
	case 5:
		System.out.println("Thursday");
		break;
	default:
		break;
	}
}
}
